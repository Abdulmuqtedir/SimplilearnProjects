package assistedjava;

/*The Enhanced For Loop is used to traverse array or collection in java. 
 It is easier to use than a  simple For Loop because we don't need to increment value
 and use subscript notation. */

public class EnhancedForLoop {

	public static void main(String[] args) {
		
		int arr[] = {12,23,44,56,78};
		for (int i:arr)
		{
			System.out.println(i);
		}

	}

}
