package assistedjava;

/*For Loop is used when the number of iterations is fixed and the user
knows exactly how many times the block of code has to be executed. */

public class ForLoop {

	public static void main(String[] args) {
	    for (int x = 2; x <= 4; x++)
		{
			System.out.println("Value of x: " + x);
		}

	}

}
